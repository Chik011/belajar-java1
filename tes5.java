class tes5 {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        int pertambahan = a + b;
        System.out.println(a + b + " di tambah, hasilnya: " + pertambahan);

        int pengurangan = a - b;
        System.out.println(a + b + " di kurangin, hasilnya: " + pengurangan);

        int perkalian = a * b;
        System.out.println(a + b + " di kali, hasilnya: " + perkalian);

        int pembagian = a / b;
        System.out.println(a + b + " di bagi, hasilnya: " + pembagian);

        int modulus = a % b;
        System.out.println(a + b + " di modulus, hasilnya: " + modulus);
    }
}